    /**
     * Comprueba si se han cometido mas de 6 fallos o si la palabra ha sido
     * adivinada
     */
    public void comprobarEstado() {
        if (oAhorcado.getFallos() >= 6) {
            gameOver();
            oAhorcado.getoJugador().setPalabrasFalladas(oAhorcado.getoJugador().getPalabrasFalladas() + 1);
            mensaje("Has perdido");
        } else {
            int count = 0;
            for (int i = 0; i < oAhorcado.getPalabraOculta().length(); i++) {
                if (oAhorcado.getPalabraOculta().charAt(i) != '_') {
                    count++;
                }
            }
            if (count == oAhorcado.getPalabraOculta().length()) {
                gameOver();
                oAhorcado.getoJugador().setPalabrasAcertadas(oAhorcado.getoJugador().getPalabrasAcertadas() + 1);
                mensaje("Has ganado");
            }
        }
        oAhorcado.guardarJugador();
    }

