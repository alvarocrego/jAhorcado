    private void jMenuItemJugadorEstadisticasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemJugadorEstadisticasActionPerformed
        int pa = oAhorcado.getoJugador().getPalabrasAcertadas();
        int pf = oAhorcado.getoJugador().getPalabrasFalladas();
        int la = oAhorcado.getoJugador().getLetrasAcertadas();
        int lf = oAhorcado.getoJugador().getLetrasFalladas();
        mensaje("Puntuación: " + (pa * 200 + (-pf * 50) + la * 5 + (-lf)) + "\n"
                + "Palabras Acertadas: " + pa + "\n"
                + "Palabras Falladas: " + pf + "\n"
                + "Letras Acertadas: " + la + "\n"
                + "Letras Falladas: " + lf);
    }//GEN-LAST:event_jMenuItemJugadorEstadisticasActionPerformed

